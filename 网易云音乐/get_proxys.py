def parse_proxys():
    content = get_proxys()

    soup = BeautifulSoup(content, features='lxml')
    trs = soup.select('tr')

    for tr in trs[1:]:
        ip = tr.select('td')[1].string
        port = tr.select('td')[2].string
        proxy = ip + ":" + port
        yield proxy


def write_proxy():
    proxies = []

    for proxy in parse_proxys():
        proxies.append(proxy)

    return proxies
